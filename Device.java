class Device {
    public static void powerOn() {
        System.out.println("Powering on device...");
    }

    public static void powerOff() {
        System.out.println("Powering off device...");
    }

    public void charge() {
        System.out.println("Charging device...");
    }

    public void operate() {
        System.out.println("Operating device...");
    }

    public void reset() {
        System.out.println("Resetting device...");
    }

    public void update() {
        System.out.println("Updating device software...");
	}
		
	public void delete(){
		System.out.println("deleting the software...");
    }
}

