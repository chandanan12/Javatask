class Movie {
    public static void start() {
        System.out.println("Starting the movie...");
    }

    public static void end() {
        System.out.println("Ending the movie...");
    }

    public void playScene() {
        System.out.println("Playing scene...");
    }

    public void showCredits() {
        System.out.println("Showing credits...");
    }

    public void rateMovie() {
        System.out.println("Rating the movie...");
    }

    public void pause() {
        System.out.println("Pausing movie...");
	}
		
	public void time(){
	    System.out.println("movie showing time");
    }
}

