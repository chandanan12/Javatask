class Building {
    String name;
    int floors;
    double height;
    boolean hasLift;
    char category;

    public Building(String name, int floors, double height, boolean hasLift, char category) {
        this.name = name;
        this.floors = floors;
        this.height = height;
        this.hasLift = hasLift;
        this.category = category;
    }
}