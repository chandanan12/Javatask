class Vehicle {
    String brand;
    int modelYear;
    double mileage;
    boolean isElectric;
    char fuelType;

    public Vehicle(String brand, int modelYear, double mileage, boolean isElectric, char fuelType) {
        this.brand = brand;
        this.modelYear = modelYear;
        this.mileage = mileage;
        this.isElectric = isElectric;
        this.fuelType = fuelType;
    }
}