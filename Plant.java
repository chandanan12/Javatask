class Plant {
    public static void water() {
        System.out.println("Watering plant...");
    }

    public static void fertilize() {
        System.out.println("Adding fertilizer...");
    }

    public void grow() {
        System.out.println("Plant is growing...");
    }

    public void photosynthesis() {
        System.out.println("Performing photosynthesis...");
    }

    public void bloom() {
        System.out.println("Plant is blooming...");
    }

    public void shedLeaves() {
        System.out.println("Shedding leaves...");
    }
}

