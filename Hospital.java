class Hospital {
    public static void open() {
        System.out.println("Opening hospital...");
    }

    public static void close() {
        System.out.println("Closing hospital...");
    }

    public void admitPatient() {
        System.out.println("Admitting patient...");
    }

    public void dischargePatient() {
        System.out.println("Discharging patient...");
    }

    public void operate() {
        System.out.println("Operating patient...");
    }

    public void maintainRecords() {
        System.out.println("Maintaining hospital records...");
    }
}

