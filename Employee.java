class Employee {
    String name;
    int id;
    double salary;
    boolean isPermanent;
    char grade;

    public Employee(String name, int id, double salary, boolean isPermanent, char grade) {
        this.name = name;
        this.id = id;
        this.salary = salary;
        this.isPermanent = isPermanent;
        this.grade = grade;
    }
}